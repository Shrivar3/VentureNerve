export function computeRAR(startup) {
  return startup.expectedIRR * (1 - startup.pd12m);
}

export function rankStartups(startups, { minIRR, maxPD }) {
  return startups
    .filter((s) => s.expectedIRR >= minIRR && s.pd12m <= maxPD)
    .map((s) => ({ ...s, rar: computeRAR(s) }))
    .sort((a, b) => b.rar - a.rar);
}
